const getLast30DaysDates = () => {
    const dates = [];
    const today = new Date();
    for (let i = 0; i < 30; i++) {
        const pastDate = new Date(today);
        pastDate.setDate(today.getDate() - i);
        dates.push(pastDate.getDate() + '日'); // Adding "日" to each date
    }
    return dates.reverse();
}


const get30DaysActivityLists=(records)=> {
    const registrationCountsList = Array(30).fill(0);
    const cancellationCountsList = Array(30).fill(0);

    const today = new Date();

    records.forEach(record => {
        const recordDate = new Date(record.time);
        const dayDiff = Math.floor((today - recordDate) / (1000 * 60 * 60 * 24));

        if (dayDiff >= 0 && dayDiff < 30) {
            if (record.action === '注册') {
                registrationCountsList[29 - dayDiff]++;
            } else if (record.action === '注销') {
                cancellationCountsList[29 - dayDiff]++;
            }
        }
    });

    return {
        registrationCountsList,
        cancellationCountsList
    };
}


